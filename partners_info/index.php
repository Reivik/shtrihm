<p>Последующее повышение квалификации на льготных условиях по основным направлениям: техническое обучение для механиков, внедрение программного обеспечения, комплексные вопросы автоматизации торговли и общепита. В течение года мы обучим трёх Ваших менеджеров по продажам бесплатно.</p>
 
